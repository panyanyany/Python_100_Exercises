名人名言 = data["famous"]  # a 代表前面垫话，b代表后面垫话
前面垫话 = data["before"]  # 在名人名言前面弄点废话
后面垫话 = data['after']  # 在名人名言后面弄点废话
废话 = data['bosh']  # 代表文章主要废话来源

主题 = input('输入主题：')

文章 = ""
while len(文章) < 100:
    分支 = random.randint(0, 100)
    if 分支 < 5:
        文章 += "\n\n"  # 换行
    elif 分支 < 20:
        random.shuffle(名人名言)
        名言 = 名人名言[0]
        名言 = 名言.replace("a", random.choice(前面垫话))
        名言 = 名言.replace("b", random.choice(后面垫话))
        文章 += 名言
    else:
        random.shuffle(废话)
        文章 += 废话[0]

文章 = 文章.replace('x', 主题)
print(文章)
